let theCurrent = new Date();
export let theCurrentDate = {
  gregorianDate: new Date(),
  currentHijriDate: theCurrent.toHijri(),
  updateDate() {
    let date = new Date();
    let hijri = date.toHijri();
    hijri.addDay();
    let gor = hijri.toGregorian();
    this.gregorianDate = new Date(gor);
    this.currentHijriDate = hijri;
  },
  getCurrentDateHijri() {
    return this.currentHijriDate._date;
  },
  getCurrentMonthHijri() {
    return this.currentHijriDate._month;
  },
  getCurrentYearHijri() {
    return this.currentHijriDate._year;
  },
  yearHijri: theCurrent.toHijri()._year,
};
// Get the century for year
export function getCentury(year) {
  switch (true) {
    case year < 31:
      return "1";
      break;
    case year > 30 && year < 61:
      return "2";
      break;
    case year > 60 && year < 91:
      return "3";
      break;
    case year > 90 && year < 121:
      return "4";
      break;
    case year > 120 && year < 151:
      return "5";
      break;
    case year > 150 && year < 181:
      return "6";
      break;
    case year > 180 && year < 211:
      return "7";
      break;
  }
}
// CountOfMonthDays
export function countDayOfMonth(month, yearNumber) {
  if (month % 2 == 0) {
    if (month == 12 && leapYears.includes(yearNumber))
      return 30; // Year Is Leap
    else return 29; // Even months
  } else return 30; // odd months
}
export function countOfMonthDays(monthCount, yearNumber) {
  let result = 0;
  for (let i = 1; i <= monthCount; i++) {
    if (i % 2 == 0) {
      if (i == 12 && leapYears.includes(yearNumber)) result += 30;
      else result += 29;
    } else result += 30;
  }
  return result;
}
export function displayListOfMonths() {
  let listOfMonth = document.getElementById("listOfMonth");
  listOfMonth.innerHTML = "";
  for (const month of Object.keys(months)) {
    listOfMonth.innerHTML += `<option value="${month}">${months[month]}</option>`;
  }
}
